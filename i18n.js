function t(key, replacements = {}) {
    const lang = localStorage.getItem('lang') || 'az';
    let translation = (translations[lang] && translations[lang][key]) ? translations[lang][key] : translations['az'][key] || key;
    Object.keys(replacements).forEach(placeholder => {
        translation = translation.replace(new RegExp(`{${placeholder}}`, 'g'), replacements[placeholder]);
    });
    return translation;
}

function translatePage() {
    const lang = localStorage.getItem('lang') || 'az';
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        let translation = (translations[lang] && translations[lang][key]) ? translations[lang][key] : translations['az'][key] || key;
        if (!translation) return;
        const count = element.getAttribute('data-key-count');
        if (count) {
            translation = translation.replace('{count}', count);
        }
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            if (element.placeholder) element.placeholder = translation;
        } else if (element.tagName === 'OPTION') {
            if (element.value === "") element.innerText = translation;
        } else {
            element.innerHTML = translation;
        }
    });
    document.querySelectorAll('.lang-btn-text').forEach(btn => {
        btn.innerText = lang.toUpperCase();
    });
}

function _i18n_setLanguage(lang) {
    localStorage.setItem('lang', lang);
    location.reload(); 
}

document.addEventListener('DOMContentLoaded', () => {
    translatePage();
    
    document.querySelectorAll('.lang-switcher').forEach(langBtn => {
        const langDropdown = langBtn.querySelector('.lang-dropdown');
        if (!langDropdown) return;
        langBtn.addEventListener('click', (e) => {
            e.stopPropagation(); 
            document.querySelectorAll('.lang-dropdown.show').forEach(dropdown => {
                if (dropdown !== langDropdown) {
                    dropdown.classList.remove('show');
                }
            });
            langDropdown.classList.toggle('show');
        });
    });
    
    window.addEventListener('click', (e) => {
        if (!e.target.closest('.lang-switcher')) {
            document.querySelectorAll('.lang-dropdown.show').forEach(dropdown => {
                dropdown.classList.remove('show');
            });
        }
    });

    document.querySelectorAll('.lang-dropdown a[data-lang]').forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = e.currentTarget.getAttribute('data-lang');
            if (lang) {
                _i18n_setLanguage(lang);
            }
        });
    });
});